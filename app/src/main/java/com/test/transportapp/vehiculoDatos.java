package com.test.transportapp;

public class vehiculoDatos {

    String area_vehiculo, marca, modelo, no_Pasajeros, placa;

    public vehiculoDatos() {

    }

    public String getArea_vehiculo() {
        return area_vehiculo;
    }

    public void setArea_vehiculo(String area_vehiculo) {
        this.area_vehiculo = area_vehiculo;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public String getNo_Pasajeros() {
        return no_Pasajeros;
    }

    public void setNo_Pasajeros(String no_Pasajeros) {
        this.no_Pasajeros = no_Pasajeros;
    }

    public String getPlaca() {
        return placa;
    }

    public void setPlaca(String placa) {
        this.placa = placa;
    }
}
